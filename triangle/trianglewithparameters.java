package triangle;

public class trianglewithparameters {
    int side1=3;
    int side2=4;
    int side3=5;
    int perimeter=side1+side2+side3;
    int area=side1*side2/2;


    public trianglewithparameters(int side1, int side2, int side3) {
        this.side1 = side1;
        this.side2 = side2;
        this.side3 = side3;
    }
}
